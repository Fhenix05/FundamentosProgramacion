'''
Ejercicio 6:
  Programa que saque la media de tres cantidades 
Entradas: 
  Numero a: int
  Numero b: int
  Numero c: int
Salidas:
  Media
'''

numeroa = input("Insertar el numero: ")
numeroa = int(numeroa)
numerob = input("Insertar el numero: ")
numerob = int(numerob)
numeroc = input("Insetar numero ")
